"""
Decode Figma analysis: convert box-drawing UTF-8 display artifacts to readable Cyrillic.
"""
import json

NODES_FILE = "docs/figma_nodes.json"


def collect_text(node, path=""):
    results = []
    current_path = f"{path}/{node.get('name', '')}"
    if node.get("type") == "TEXT":
        results.append({
            "id": node.get("id"),
            "name": node.get("name"),
            "chars": node.get("characters", ""),
            "path": current_path,
        })
    for child in node.get("children", []):
        results.extend(collect_text(child, current_path))
    return results


def decode_frame(data, node_id_key, label):
    node_data = data["nodes"].get(node_id_key)
    if not node_data:
        return f"\n### {label} — не найден в nodes\n"
    doc = node_data["document"]
    lines = [f"\n### {label}  (node_id={node_id_key}, frame_id={doc.get('id')})"]
    texts = collect_text(doc)
    if not texts:
        lines.append("  (нет TEXT-слоёв)")
    for t in texts:
        name = t["name"]
        chars = t["chars"].replace("\n", " / ").replace("\u2028", " ")
        lines.append(f"  • [{t['id']}] name={name!r}")
        lines.append(f"    sample: {chars!r}")
    return "\n".join(lines)



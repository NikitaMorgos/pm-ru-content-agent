def _pick_font() -> Path:
    for p in _FONT_CANDIDATES:
        if p.is_file():
            return p
    raise FileNotFoundError(
        "Не найден TTF-шрифт с кириллицей. "
        "Укажите путь в переменной CONTENT_AGENT_PDF_FONT."
    )


def _font_path() -> Path:
    env = __import__("os").environ.get("CONTENT_AGENT_PDF_FONT")
    if env:
        p = Path(env)
        if p.is_file():
            return p
    return _pick_font()



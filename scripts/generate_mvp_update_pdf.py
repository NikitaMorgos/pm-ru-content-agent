class Deck(FPDF):
    def __init__(self, font_path: Path) -> None:
        super().__init__(format="A4")
        self.font_path = font_path
        self.set_auto_page_break(auto=True, margin=20)

    def header(self) -> None:  # noqa: ANN401
        return

    def footer(self) -> None:
        self.set_y(-14)
        self.set_font("DocFont", "", 8)
        self.set_text_color(130, 130, 130)
        self.cell(
            0, 8,
            f"PM-RU Content Agent  |  MVP update 22 апреля 2026  |  Стр. {self.page_no()}",
            align="C",
        )


def build_pdf(font_path: Path, out: Path) -> None:
    pdf = Deck(font_path)
    pdf.add_font("DocFont", "", str(font_path))

    for title, subtitle, bullets in SLIDES:
        pdf.add_page()
        ml = pdf.l_margin

        # Title
        pdf.set_font("DocFont", "", 17)
        pdf.set_text_color(15, 55, 110)
        pdf.set_x(ml)
        pdf.multi_cell(0, 10, title)

        # Subtitle
        if subtitle:
            pdf.set_font("DocFont", "", 10)
            pdf.set_text_color(100, 100, 100)
            pdf.set_x(ml)
            pdf.multi_cell(0, 6, subtitle)

        pdf.ln(3)
        pdf.set_draw_color(180, 180, 200)
        pdf.set_line_width(0.3)
        pdf.line(ml, pdf.get_y(), ml + 170, pdf.get_y())
        pdf.ln(4)

        for text, level in bullets:
            if text == "":
                pdf.ln(2)
                continue
            if level == 0:
                pdf.set_font("DocFont", "", 11)
                pdf.set_text_color(25, 25, 25)
                pdf.set_left_margin(ml)
                pdf.set_x(ml)
                pdf.multi_cell(0, 6.5, f"• {text}")
            else:
                pdf.set_font("DocFont", "", 9)
                pdf.set_text_color(70, 70, 70)
                pdf.set_left_margin(ml + 7)
                pdf.set_x(ml + 7)
                pdf.multi_cell(0, 5.5, f"  {text}")
                pdf.set_left_margin(ml)
            pdf.ln(0.5)

    out.parent.mkdir(parents=True, exist_ok=True)
    pdf.output(str(out))


def main() -> int:
    try:
        fp = _font_path()
    except FileNotFoundError as e:
        print(e, file=sys.stderr)
        return 1
    build_pdf(fp, OUT_PATH)
    print(f"Wrote {OUT_PATH}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
